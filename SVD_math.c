#include "SVD_math.h"

double svd_tan(double x){
  double result =  tan(x);
  return result;
}

double svd_cos(double x){
  double result =  cos(x);
  return result;
}

double svd_sin(double x){
  double result =  sin(x);
  return result;
}

double svd_atan2(double x, double y){
  double result =  atan2(x,y);
  return result;
}
